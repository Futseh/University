from math import pi, exp, sqrt

def gauss(x, m=0, s=1):
  n = 1

  x_values = []
  gaussian = []

  values = [m - 5*s, m + 5*s]

  for i in range(values[0], values[1], n):
    e = exp((-1 / 2.0) * (((i - m) / float(s))**2))
    f = (1 / (sqrt(2 * pi) * float(s)))
    fe = f * e

    x_values.append(i)
    gaussian.append(fe)

  print('x \t\t f(x)')
  
  for i in range(len(gaussian)):
    print('%g \t\t %e' % (x_values[i], gaussian[i]))

gauss(10, 2, 4)

