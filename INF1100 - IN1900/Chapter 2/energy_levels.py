# Exercise 2.6

me = 9.1094 * 10**(-31) # Electron mass in kg
e = 1.6022 * 10**(-19) # Elementary charge in C
e0 = 8.8542 * 10**(-12) # Eletrical permittivity of a vacuum in C^2*s^2/kg*m^3
h = 6.6261 * 10**(-34) # Plancks constant Js
n = 1.0 # The "energy level" you are on

# Exercise 2.6 a

print('%s \t %s' % ('Level', 'Energy'))
while n < 21:
  En = -((me * e**4)/(8 * e0**2 * h**2)) * (1 / n**2)
  print('---------------------')
  print('%g |\t %g' % (n, En))
  n += 1
print('\n')


# Exercise 2.6 b

ni = 5
nf = 5

E = -((me * e**4)/(8 * e0**2 * h**2))

print('%s \t %s \t %s' % ('Level from', 'Level to', 'Energy'))
for a in range(nf):
  for b in range(ni):
    diff = ((1 / float((b + 1)**2)) - (1 / float((a + 1)**2)))
    energy = E * diff

    print('%g \t\t %g \t\t %g' % (b + 1, a + 1, energy))

